import UIKit

//在类的构造器前添加required修饰符表明所有该类的子类都必须实现该构造器：
//在重写父类中必要的指定构造器时，不需要添加override修饰符：
class Document {
    var name: String?
    // 该构造器创建了一个 name 属性的值为 nil 的 document 实例
    init() {}
    // 该构造器创建了一个 name 属性的值为非空字符串的 document 实例
    init?(name: String) {
        self.name = name
        if name.isEmpty { return nil }
    }
}
